export const getFeaturedProducts = (): Product[] => products.filter(p => p.featured);

export const getProductById = (id: string): Product | undefined => products.find(p => p.id === id);

export const getProductsByCategory = (category: string): Product[] =>
  products.filter(p => p.category === category);

export const getRelatedProducts = (product: Product, limit = 4): Product[] =>
  products.filter(p => p.id !== product.id && (p.category === product.category || p.petType === product.petType)).slice(0, limit);

export const getAllBrands = (): string[] =>
  [...new Set(products.map(p => p.brand))].sort();

export const getCategoryList = (): string[] =>
  [...new Set(products.map(p => p.category))].sort();

export const getPetTypeList = (): string[] =>
  [...new Set(products.map(p => p.petType))].sort();

export const getPriceRange = (): { min: number; max: number } => ({
  min: Math.min(...products.map(p => p.price)),
  max: Math.max(...products.map(p => p.price)),
});
